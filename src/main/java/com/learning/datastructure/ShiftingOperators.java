package com.learning.datastructure;
public class ShiftingOperators {
    public static void main(String[] args) {
        int i = 1;
        System.out.println("i " + i);
        System.out.println("i << 1 " + (i << 2));
        System.out.println("i << 2 " + (i << 2));
        System.out.println("i >> 2 " + (i >> 2));
    }
}
